namespace GsmCalculator.Models;

/// <summary>
/// Тип плотности виджета.
/// </summary>
public enum DensityMode
{
    /// <summary>Плотность фиксированная и не редактируется в виджете (например, Масла = 0.9).</summary>
    Fixed,

    /// <summary>Плотность переменная — пользователь может изменить её в виджете (например, АИ-92).</summary>
    Variable
}

/// <summary>
/// Поведение приложения при запуске (настройка в окне Настроек).
/// </summary>
public enum StartupBehavior
{
    /// <summary>Спрашивать диалогом "Продолжить прошлую сессию?".</summary>
    AlwaysAsk,

    /// <summary>Всегда восстанавливать прошлую сессию без вопросов.</summary>
    AlwaysContinue,

    /// <summary>Всегда стартовать с чистого листа.</summary>
    AlwaysFresh
}

/// <summary>
/// Цветовая тема калькулятора.
/// Имена темы соответствуют файлам в Resources/Themes (LightTheme.xaml и т.д.).
/// </summary>
public enum ColorTheme
{
    Light,
    Dark,
    Blue
}

/// <summary>
/// Поддерживаемые языки интерфейса.
/// </summary>
public enum AppLanguage
{
    Russian,
    English
}

/// <summary>
/// Режим калькулятора:
/// - Classic: операции применяются слева направо (как Windows Calc «Стандартный»).
///   Пример: 5 − 1 × 3 = 12 (сначала 5−1=4, потом 4×3=12).
/// - Engineering: соблюдается приоритет × ÷ выше + −.
///   Пример: 5 − 1 × 3 = 2.
/// </summary>
public enum CalculatorMode
{
    Classic,
    Engineering
}

/// <summary>
/// Режим округления видимых/копируемых значений в калькуляторе.
///
/// Округление применяется ПО ПРИНЦИПУ "K.5 = Y" (см. ТЗ v1.2):
/// каждый раз когда нажат '=', результат округляется и СТАНОВИТСЯ
/// аккумулятором — то есть в дальнейших вычислениях участвует уже
/// округлённое число (в духе ГСМ-учёта: «хвосты откидываются»,
/// и в учёте уже используется округлённое целое).
///
/// Также округление применяется:
/// - при копировании в буфер обмена,
/// - при вставке результата из виджета в калькулятор,
/// - при записи в историю.
/// </summary>
public enum RoundingMode
{
    /// <summary>Без округления — показывается полная точность (поведение v1.0/v1.1).</summary>
    None,
    /// <summary>До целых.</summary>
    Integer,
    /// <summary>До одной десятой.</summary>
    OneTenth
}

namespace GsmCalculator.Services;

/// <summary>
/// Debounce: «склеивает» серию быстрых вызовов в один отложенный.
/// Каждый <see cref="Debounce"/> рестартит внутренний таймер; когда таймер
/// доходит до конца — выполняется последний переданный callback.
///
/// Используется для авто-сохранения изменений плотности/округления виджета
/// (юзер двигает слайдер — десятки апдейтов в секунду, нам нужен один write
/// в конце).
/// </summary>
public interface IDebouncer
{
    /// <summary>
    /// Запоминает callback и (пере)запускает таймер. Если был запланирован
    /// другой callback, он не выполнится — последний выигрывает.
    /// </summary>
    void Debounce(Action callback);

    /// <summary>Отменяет ожидающий callback (если был). Ничего не делает если ничего не ждёт.</summary>
    void Cancel();

    /// <summary>
    /// Если callback ожидает — выполняет его НЕМЕДЛЕННО (синхронно)
    /// и очищает ожидание. Используется при закрытии виджета чтобы
    /// не потерять последнее изменение.
    /// </summary>
    void Flush();
}

/// <summary>
/// Создаёт <see cref="IDebouncer"/> с заданной задержкой.
/// Каждый виджет получает свой debouncer — состояние таймера
/// привязано к конкретному окну.
/// </summary>
public interface IDebouncerFactory
{
    IDebouncer Create(TimeSpan delay);
}
